import os
from reportlab.pdfgen import canvas
from reportlab.lib.pagesizes import A4
from reportlab.pdfbase import pdfmetrics
from reportlab.pdfbase.ttfonts import TTFont

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
FONT_DIR = os.path.join(ROOT, "tools", "assets", "fonts")
OUT = os.path.join(ROOT, "docs", "julian_research_report.pdf")

pdfmetrics.registerFont(TTFont("S",  os.path.join(FONT_DIR, "Sarabun-Regular.ttf")))
pdfmetrics.registerFont(TTFont("SB", os.path.join(FONT_DIR, "Sarabun-Bold.ttf")))

# ── palette (CLAUDE.md brand — ห้ามเปลี่ยน) ──
GOLD=(0.729,0.514,0.271); NAVY=(0.122,0.176,0.369); MAROON=(0.600,0.329,0.404)
DEEP=(0.039,0.173,0.353); INK=(0.157,0.169,0.275); GREY=(0.43,0.43,0.47)
LIGHT=(0.957,0.949,0.973); WHITE=(1,1,1)

PW, PH = A4
ML, MR, MT, MB = 56, 56, 58, 46
CW = PW - ML - MR

class R:
    def __init__(self):
        self.c = canvas.Canvas(OUT, pagesize=A4)
        self.y = PH - MT
        self.page = 0
        self.secno = 0
        self._pagemin = []
        self._newpage(cover=True)

    # ── low level ──
    def _fill(self, rgb): self.c.setFillColorRGB(*rgb)
    def _stroke(self, rgb): self.c.setStrokeColorRGB(*rgb)
    def sw(self, t, f, s): return pdfmetrics.stringWidth(t, f, s)

    def _footer(self):
        if self.page == 1: return
        self.c.setFont("S", 8); self._fill(GREY)
        self.c.drawString(ML, 28, "โหราทาส JULIAN · งานวิจัยโหราศาสตร์เชิงประจักษ์")
        self.c.drawRightString(PW-MR, 28, f"หน้า {self.page}")

    def _newpage(self, cover=False):
        if self.page > 0:
            self._pagemin.append(round(self.y, 1))
            self._footer(); self.c.showPage()
        self.page += 1
        self.y = PH - MT

    def need(self, h):
        if self.y - h < MB:
            self._newpage()

    def wrap(self, text, font, size, maxw):
        out, line = [], ""
        for w in text.split(" "):
            t = (line + " " + w).strip()
            if self.sw(t, font, size) > maxw and line:
                out.append(line); line = w
            else:
                line = t
        if line: out.append(line)
        return out or [""]

    # ── blocks ──
    def h1(self, text):
        self.secno += 1
        if self.y < PH*0.58:      # section ใหม่ขอพื้นที่พอควร — ไม่ยัด 3 หัวข้อต่อหน้า
            self._newpage()
        else:
            self.y -= 12
        self._fill(NAVY); self.c.rect(ML, self.y-18, CW, 18, fill=1, stroke=0)
        self._fill(WHITE); self.c.setFont("SB", 14.5)
        self.c.drawString(ML+8, self.y-13.5, f"{self.secno}.  {text}")
        self._stroke(GOLD); self.c.setLineWidth(1.4); self.c.line(ML, self.y-18, PW-MR, self.y-18)
        self.y -= 30

    def h2(self, text):
        self.need(26)
        self.y -= 4
        self._fill(MAROON); self.c.setFont("SB", 12.5)
        self.c.drawString(ML, self.y-11, text)
        self.y -= 20

    def body(self, text, size=10.8, leading=16.4, gap=7):
        for ln in self.wrap(text, "S", size, CW):
            self.need(leading)
            self._fill(INK); self.c.setFont("S", size)
            self.c.drawString(ML, self.y-size, ln)
            self.y -= leading
        self.y -= gap

    def bullet(self, text, size=10.8, leading=16.4, indent=14):
        lines = self.wrap(text, "S", size, CW-indent-6)
        for i, ln in enumerate(lines):
            self.need(leading)
            if i == 0:
                self._fill(GOLD); self.c.setFont("SB", size)
                self.c.drawString(ML+indent-8, self.y-size, "•")
            self._fill(INK); self.c.setFont("S", size)
            self.c.drawString(ML+indent+4, self.y-size, ln)
            self.y -= leading
        self.y -= 2

    def callout(self, title, text, color=GOLD, size=10.2, leading=14):
        lines = self.wrap(text, "S", size, CW-26)
        h = 16 + 6 + len(lines)*leading
        self.need(h+6)
        top = self.y
        self._fill(LIGHT); self._stroke(color); self.c.setLineWidth(0.6)
        self.c.rect(ML, top-h, CW, h, fill=1, stroke=1)
        self._fill(color); self.c.rect(ML, top-h, 3, h, fill=1, stroke=0)
        self._fill(color); self.c.setFont("SB", 10.6)
        self.c.drawString(ML+12, top-15, title)
        yy = top - 15 - 16
        self._fill(INK); self.c.setFont("S", size)
        for ln in lines:
            self.c.drawString(ML+12, yy, ln); yy -= leading
        self.y = top - h - 6

    def table(self, headers, rows, widths, fs=9.3, lead=12.6):
        # scale widths to CW
        tot = sum(widths); widths = [w/tot*CW for w in widths]
        self.need(20)
        # header
        self._fill(NAVY); self.c.rect(ML, self.y-15, CW, 15, fill=1, stroke=0)
        self._fill(WHITE); self.c.setFont("SB", fs)
        x = ML
        for hh, w in zip(headers, widths):
            self.c.drawString(x+4, self.y-11, hh); x += w
        self.y -= 15
        fill = False
        for row in rows:
            cells = [self.wrap(str(c), "S", fs, w-8) for c, w in zip(row, widths)]
            rh = max(len(cl) for cl in cells)*lead + 6
            if self.y - rh < MB:
                self._newpage()
                self._fill(NAVY); self.c.rect(ML, self.y-15, CW, 15, fill=1, stroke=0)
                self._fill(WHITE); self.c.setFont("SB", fs)
                x = ML
                for hh, w in zip(headers, widths): self.c.drawString(x+4, self.y-11, hh); x += w
                self.y -= 15
            self._fill(LIGHT if fill else WHITE); self.c.rect(ML, self.y-rh, CW, rh, fill=1, stroke=0)
            x = ML
            for cl, w in zip(cells, widths):
                self._fill(INK); self.c.setFont("S", fs)
                yy = self.y - 12
                for ln in cl:
                    self.c.drawString(x+4, yy, ln); yy -= lead
                x += w
            self._stroke((0.85,0.85,0.87)); self.c.setLineWidth(0.2)
            self.c.line(ML, self.y-rh, PW-MR, self.y-rh)
            self.y -= rh; fill = not fill
        self.y -= 6

    def barchart(self, labels, values, unit=""):
        rowh = 16
        self.need(len(labels)*rowh + 6)
        mx = max(values) if values else 1
        x0 = ML+64; barmax = CW-64-70
        for lab, v in zip(labels, values):
            self._fill(INK); self.c.setFont("S", 9)
            self.c.drawRightString(ML+58, self.y-9, str(lab))
            w = max(2, barmax*(v/mx))
            self._fill(GOLD); self.c.rect(x0, self.y-11, w, 7, fill=1, stroke=0)
            self._fill(GREY); self.c.setFont("S", 8.5)
            self.c.drawString(x0+w+4, self.y-9.5, f"{v:,}{unit}")
            self.y -= rowh
        self.y -= 6

    # ── cover ──
    def cover(self):
        self._fill(NAVY); self.c.rect(0,0,PW,PH, fill=1, stroke=0)
        self._fill(DEEP); self.c.rect(0,0,PW,PH*0.42, fill=1, stroke=0)
        self._fill(MAROON); self.c.rect(0,0,PW,PH*0.14, fill=1, stroke=0)
        self._fill(GOLD); self.c.setFont("SB", 46)
        self.c.drawCentredString(PW/2, PH-220, "โหราทาส")
        self._fill(WHITE); self.c.setFont("SB", 22)
        self.c.drawCentredString(PW/2, PH-250, "JULIAN")
        self._fill(GOLD); self.c.setFont("SB", 17)
        self.c.drawCentredString(PW/2, PH-300, "งานวิจัยโหราศาสตร์เชิงประจักษ์")
        self._fill(WHITE); self.c.setFont("S", 13)
        self.c.drawCentredString(PW/2, PH-322, "ฐานข้อมูล · เครือญาติ · ระเบียบวิธี")
        self._fill((0.86,0.86,0.9)); self.c.setFont("S", 11)
        self.c.drawCentredString(PW/2, 250, "ฉบับสำหรับผู้อ่านทั่วไป (ระดับปริญญาตรี)")
        self.c.drawCentredString(PW/2, 232, "อธิบายละเอียด เข้าใจง่าย ไม่ต้องมีพื้นฐานสถิติหรือโหราศาสตร์")
        self._fill(WHITE); self.c.setFont("S", 10)
        self.c.drawCentredString(PW/2, 80, "กรอบทฤษฎี: TALS — โหราศาสตร์ไทยแนวตรรกะ")
        self.c.drawCentredString(PW/2, 64, "ผู้วางรากฐาน: ยืนยง นาวาสมุทร (แดง เมืองตราด)")

    def save(self):
        self._pagemin.append(round(self.y, 1))
        self._footer(); self.c.showPage(); self.c.save()
        print(f"OK → {OUT} ({os.path.getsize(OUT):,} bytes, {self.page} pages)")
        bad = [(i+1, m) for i, m in enumerate(self._pagemin) if m < MB-2]
        print(f"per-page min-y (MB={MB}): {self._pagemin}")
        print("⚠️ OVERFLOW pages:" if bad else "✓ no overflow (ทุกหน้า min-y ≥ margin)", bad or "")


